#include "util.h"

int	ft_atoui(const char *str)
{
	unsigned int	out;

	while (*str == ' ' || *str == '\n' || *str == '\t' || *str == '\v'
		|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '+')
		str++;
	out = 0;
	while ('0' <= *str && *str <= '9')
	{
		out = out * 10 + *str - '0';
		str++;
	}
	return (out);
}
